    private void fillToTable() {
        tbModel.setRowCount(0);
        if (serNV.getList().size() <= 0) {
            return;
        }
        for (NhanVien nv : serNV.getList()) {
            tbModel.addRow(new String[]{nv.getMaNV(), nv.getHoTen(), nv.getNgaySinh(),
                nv.isGioiTinh() ? "Nam" : "Nữ", serCV.tim2(nv.getMaCV()).getTenCV()});
        }
        id = 0;
        showDetail();
    }

    private void clearForm() {
        id = -1;
        tblList.clearSelection();

        lbMaNV.setText("---");
        txtHoTen.setText("");
        txtNgaySinh.setText("");

        rdoNam.setSelected(true);
        rdoNu.setSelected(false);

        cboChucVu.setSelectedIndex(0);

    }

    private void showDetail() {
        if (id < 0) {
            return;
        }

        NhanVien nv = serNV.getList().get(id);
        tblList.setRowSelectionInterval(id, id);

        lbMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtNgaySinh.setText(nv.getNgaySinh());

        rdoNam.setSelected(nv.isGioiTinh());
        rdoNu.setSelected(!nv.isGioiTinh());

        cboChucVu.setSelectedItem(serCV.tim2(nv.getMaCV()).getTenCV());

    }

    public boolean ValidateNS() {
        String ns = txtNgaySinh.getText();
        try {
            SimpleDateFormat ns2 = new SimpleDateFormat("yyyy-MM-dd");
            Date d = ns2.parse(ns);
            System.out.println(d);
            return true;
        } catch (Exception e) {
            e.printStackTrace(System.out);
            JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ, mời nhập lại!");
            return false;
        }
    }
