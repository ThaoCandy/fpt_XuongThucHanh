    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (id < 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn bản ghi nhân viên nào trên bảng!");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa nhân viên này?") == 0) {
            if (ValidateNS()) {
                String tb = serNV.sua(new NhanVien(lbMaNV.getText(), txtHoTen.getText(),
                        txtNgaySinh.getText(), rdoNam.isSelected(), serCV.tim(cboChucVu.getSelectedItem().toString()).getMaCV()));
                JOptionPane.showMessageDialog(this, tb);
                fillToTable();
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

